function inc_qty(e){
  var bookid = ($(e).attr('id')).replace('plus','');
  var curr_val = parseInt($('#qty'+bookid).val());
  curr_val = curr_val + 1;
  $('#qty'+bookid).val(curr_val);
}

function dec_qty(e){
  var bookid = ($(e).attr('id')).replace('minus','');
  var curr_val = parseInt($('#qty'+bookid).val());
  curr_val = curr_val - 1;
  if(curr_val >= 0)
    $('#qty'+bookid).val(curr_val);
}

function getBookById(bookid) {
  return books.filter(
    function(book) {
      if (book.bookid == bookid)
        return book;
    }
  );
}

